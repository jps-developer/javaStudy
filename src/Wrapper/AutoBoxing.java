package Wrapper;

public class AutoBoxing {

    public static void main(String[] args) {
        Integer iObj = 10;
        Double dObj = 3.1;

        System.out.println(iObj);
        System.out.println("dObj = " + dObj);
        
        iObj++;
        System.out.println("iObj = " + iObj);
    }
}
